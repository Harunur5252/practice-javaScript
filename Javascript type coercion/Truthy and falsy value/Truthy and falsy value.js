/*
  what types of value is false we can see by Boolean() function.
*/

let name;
console.log(name); // output is undefined
// falsy value
console.log(Boolean(false)) // output is false
console.log(Boolean(0)) // output is false
console.log(Boolean('')) // output is false
console.log(Boolean()) // output is false
console.log(Boolean(null)) // output is false
console.log(Boolean(undefined)) // output is false
console.log(Boolean(NaN)) // output is false




